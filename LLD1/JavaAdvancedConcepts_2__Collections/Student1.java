package LLD1.JavaAdvancedConcepts_2__Collections;

public class Student1 {
    private int age;
    private String name;
    private double psp;
    private double attendance;

    public Student1(int age, String name, double psp, double attendance) {
        this.age = age;
        this.name = name;
        this.psp = psp;
        this.attendance = attendance;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public double getPsp() {
        return psp;
    }

    public double getAttendance() {
        return attendance;
    }
}
